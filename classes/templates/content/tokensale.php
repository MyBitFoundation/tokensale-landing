<div id="crowdfund" class="section section__crowdfund section__inverse wow animated fadeInUp" data-wow-offset="50">
    <div class="container">
        <h2 class="h3 section__title">Why Raise via Crowdfunding?</h2>
        <p>We evaluated raising capital between a Blockchain crowdfunding campaign and traditional venture capital and concluded that utilizing an Ethereum ERC20 token would be the optimal fundraising method to achieve our vision with this platform.</p>
        <p>Blockchain crowdfunding (in this situation) is better for our investors, end users, and the overall community. Liquidity is a critical component missing in traditional financing methods, that we wanted to enable for investors. We also wanted the overall structure of our organization to be democratic, and did not want to give a centralized group of investors the power to change the fee structure or pricing model in a way that was not in the best interest of the users</p>
        <p><strong>How it Works</strong></p>
        <p>1)	Participants send funds to an Ethereum Smart Contract from an easy to use Dashboard on the MyBit website. </p>
        <p>2)	The Smart Contract mints tokens instantly and sends to user’s online account. These are available for withdrawal upon the close of the crowdsale. </p>
        <p>3)	Once completed (total duration or maximum tokens issued) the Ethereum funds are then transferred to a mult-sig escrow wallet with signing keys (Ian Worrall (MyBit), Thomas Pollan (MyBit), Fran Strajnar (BNC), and Collin Lahay (BitRated, #2 most Trusted in the World).</p>
    </div>
</div>

